/// then the coherent ArduCAM 320x240 size block. A small JPEG (~few KB) gives a much
/// higher chance of zero DVP byte errors than the ~105 KB UXGA stream.
pub fn ov2640_jpeg_qvga(dvp: &Dvp) {
    ov2640_jpeg_uxga(dvp);
    for &(reg, val) in OV2640_320X240_JPEG_SIZE {
        dvp.sccb_send(OV2640_ADDR, reg, val);
    }
}

/// Delta to bump the OV2640 from the baseline 320x240 RGB565 (`OV2640_CONFIG`) up
/// to VGA 640x480, still RGB565. Apply *after* `ov2640_init`. This is the ArduCAM
/// `640x480_JPEG` window+scaler block (sensor readout window 0x17/0x18/0x19/0x1a/
/// 0x32, DSP scaler 0xc0/0xc1/0x50-0x5c, PCLK divider 0xd3) but with the final
/// output format kept as RGB565 (0xda=0x08) instead of JPEG (0xda=0x10). The whole
/// window/scaler/PCLK set has to move together — overriding only the out-size regs
/// (0x5a/0x5b) leaves an inconsistent DSP timing config and the frame never
/// finishes. (Derived with smart-friend/codex.)
static OV2640_RGB565_VGA_DELTA: &[(u8, u8)] = &[
    // Sensor bank: VGA readout window + slow pixel clock
    (0xff, 0x01),
    (0x11, 0x01), // CLKRC: halve the pixel clock
    (0x12, 0x00),
    (0x17, 0x11),
    (0x18, 0x75),
    (0x32, 0x36),
    (0x19, 0x01),
    (0x1a, 0x97),
    (0x03, 0x0f),
    (0x37, 0x40),
    (0x4f, 0xbb),
    (0x50, 0x9c),
    (0x5a, 0x57),
    (0x6d, 0x80),
    (0x3d, 0x34),
    (0x39, 0x02),
    (0x35, 0x88),
    (0x22, 0x0a),
    (0x37, 0x40),
    (0x34, 0xa0),
    (0x06, 0x02),
    (0x0d, 0xb7),
    (0x0e, 0x01),
    // DSP bank: VGA scaler/output geometry, RGB565 format
    (0xff, 0x00),
    (0xe0, 0x04), // hold/reset DSP while changing scaler regs
    (0xc0, 0xc8),
    (0xc1, 0x96),
    (0x86, 0x3d),
    (0x50, 0x89),
    (0x51, 0x90),
    (0x52, 0x2c),
    (0x53, 0x00),
    (0x54, 0x00),
    (0x55, 0x88),
    (0x57, 0x00),
    (0x5a, 0xa0), // OUTW = 640 / 4
    (0x5b, 0x78), // OUTH = 480 / 4
    (0x5c, 0x00),
    (0xd3, 0x04), // DVP PCLK divider (slow; important for VGA RGB565)
    (0xda, 0x08), // RGB565, NOT JPEG
    (0xd7, 0x03), // keep the RGB/YUV formatter enabled (as in the working RGB path)
    (0x05, 0x00), // no bypass
    (0xe0, 0x00), // release DSP
];

/// Bump the OV2640 to VGA 640x480 RGB565. Call after `ov2640_init`.
pub fn ov2640_rgb565_vga(dvp: &Dvp) {
    for &(reg, val) in OV2640_RGB565_VGA_DELTA {
        dvp.sccb_send(OV2640_ADDR, reg, val);
    }
}

/// Delta to drop the OV2640 to QQVGA 160x120 RGB565 (small frames -> high frame
/// rate, for a serial "video" stream). Apply *after* `ov2640_init`. This is the
/// ArduCAM `160x120_JPEG` window+scaler block (the sensor window is the same QVGA
/// window the baseline already uses; only the DSP scaler/divider 0x50/0x51/0x52/
/// 0x5a/0x5b changes) with the format kept RGB565 (0xda=0x08). Same lesson as the
/// VGA delta: move the whole scaler set together inside a DSP hold (0xe0=0x04 ..
/// 0xe0=0x00), not just the out-size regs, or `get_image` hangs.
static OV2640_RGB565_QQVGA_DELTA: &[(u8, u8)] = &[
    // Sensor bank (QVGA readout window, identical to the baseline)
    (0xff, 0x01), (0x12, 0x40), (0x17, 0x11), (0x18, 0x43), (0x19, 0x00), (0x1a, 0x4b),
    (0x32, 0x09), (0x4f, 0xca), (0x50, 0xa8), (0x5a, 0x23), (0x6d, 0x00), (0x39, 0x12),
    (0x35, 0xda), (0x22, 0x1a), (0x37, 0xc3), (0x23, 0x00), (0x34, 0xc0), (0x36, 0x1a),
    (0x06, 0x88), (0x07, 0xc0), (0x0d, 0x87), (0x0e, 0x41), (0x4c, 0x00),
    // DSP bank: 160x120 scaler (0x50 divider /2 /2, out 0x5a=40/0x5b=30 -> *4), RGB565
    (0xff, 0x00), (0xe0, 0x04), (0xc0, 0x64), (0xc1, 0x4b), (0x86, 0x35), (0x50, 0x92),
    (0x51, 0xc8), (0x52, 0x96), (0x53, 0x00), (0x54, 0x00), (0x55, 0x00), (0x57, 0x00),
    (0x5a, 0x28), (0x5b, 0x1e), (0x5c, 0x00),
    (0xda, 0x08), (0xd7, 0x03), (0x05, 0x00),
    (0xe0, 0x00),
];

/// Drop the OV2640 to QQVGA 160x120 RGB565. Call after `ov2640_init`.
pub fn ov2640_rgb565_qqvga(dvp: &Dvp) {
    for &(reg, val) in OV2640_RGB565_QQVGA_DELTA {
        dvp.sccb_send(OV2640_ADDR, reg, val);
    }
}
